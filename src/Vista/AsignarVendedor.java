    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        int id = Integer.parseInt(txtId.getText());
      
        ArticuloVO articulo = (ArticuloVO) control.buscarArticulo(id);
        VendedorVO vendedor = (VendedorVO) listaVendedor.getSelectedItem();
        
        control.asociarVendedorConArticulo(articulo, vendedor);
       

        //luego de guardar, se limpian los campos automaticamente
        txtId.setText("");
        txtCodigo.setText("");
        txtNombre.setText("");
        txtDescripcion.setText("");
        txtPrecioCosto.setText("");
        txtPrecioVenta.setText("");
        txtMarca.setText("");
        listaVendedor.setSelectedItem(null);


    }//GEN-LAST:event_btnAceptarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        List<ArticuloVO> articulos = control.listarArticulos();

        DefaultTableModel model = new DefaultTableModel();

        model.addColumn("Id");
        model.addColumn("Código");
        model.addColumn("Nombre");
        model.addColumn("Descripción");
        model.addColumn("Precio Compra");
        model.addColumn("Precio Venta");
        model.addColumn("Marca");
        model.addColumn("Vendedor");

        for (ArticuloVO articulo : articulos) {
           model.addRow(new Object[]{articulo.getIdArticulo(), articulo.getCodigo(), articulo.getNombre(), articulo.getDescripcion(), articulo.getPrecioCosto(), articulo.getPrecioVenta(), articulo.getMarca().getNombre(),articulo.getVendedor()});
                }

        jTableArticulos.setModel(model);
    }//GEN-LAST:event_jButton1ActionPerformed

