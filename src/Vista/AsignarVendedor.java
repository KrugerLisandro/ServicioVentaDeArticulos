                txtPrecioVenta.setText(String.valueOf(jTableArticulos.getValueAt(filaseleccionada, 5)));
                txtMarca.setText(String.valueOf(jTableArticulos.getValueAt(filaseleccionada, 6)));
                listaVendedor.setSelectedItem(String.valueOf(jTableArticulos.getValueAt(filaseleccionada,7)));
                
