    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        int id = Integer.parseInt(txtId.getText());
        String codigo = (txtCodigo.getText());
        String nombre = (txtNombre.getText());
        String descripcion = txtDescripcion.getText();
        double precioCosto = Double.parseDouble(txtPrecioCosto.getText());
        double precioVenta = Double.parseDouble(txtPrecioVenta.getText());
        if (listaMarca.getSelectedIndex() == -1) {
            control.modificarProducto2(id, codigo, nombre, descripcion, precioCosto, precioVenta);
        } else {
            MarcaVO marca = (MarcaVO) listaMarca.getSelectedItem();
            control.modificarProducto(id, codigo, nombre, descripcion, precioCosto, precioVenta, marca);}

        //luego de guardar, se limpian los campos automaticamente
        txtId.setText("");
        txtCodigo.setText("");
        txtNombre.setText("");
        txtDescripcion.setText("");
        txtPrecioCosto.setText("");
        txtPrecioVenta.setText("");
        listaMarca.setSelectedItem(null);

    }//GEN-LAST:event_btnAceptarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        List<ArticuloVO> articulos = control.listarArticulos();

        DefaultTableModel model = new DefaultTableModel();

        model.addColumn("Id");
        model.addColumn("Código");
        model.addColumn("Nombre");
        model.addColumn("Descripción");
        model.addColumn("Precio Compra");
        model.addColumn("Precio Venta");
        model.addColumn("Marca");

        for (ArticuloVO articulo : articulos) {
            model.addRow(new Object[]{articulo.getIdArticulo(), articulo.getCodigo(), articulo.getNombre(), articulo.getDescripcion(), articulo.getPrecioCosto(), articulo.getPrecioVenta(), articulo.getMarca().getNombre()});
        }

        jTableArticulos.setModel(model);
    }//GEN-LAST:event_jButton1ActionPerformed

