    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int filaseleccionada;
        try {
            filaseleccionada = jTableArticulos.getSelectedRow();
            if (filaseleccionada == -1){
                JOptionPane.showMessageDialog(null, "No ha seleccionado ninguna fila.");
            } else {
                
                txtId.setText(String.valueOf(jTableArticulos.getValueAt(filaseleccionada, 0)));
                txtCodigo.setText(String.valueOf(jTableArticulos.getValueAt(filaseleccionada, 1)));
                txtNombre.setText(String.valueOf(jTableArticulos.getValueAt(filaseleccionada, 2)));
                txtDescripcion.setText(String.valueOf(jTableArticulos.getValueAt(filaseleccionada, 3)));
                txtPrecioCosto.setText(String.valueOf(jTableArticulos.getValueAt(filaseleccionada, 4)));
                txtPrecioVenta.setText(String.valueOf(jTableArticulos.getValueAt(filaseleccionada, 5)));
                listaMarca.setSelectedItem(jTableArticulos.getValueAt(filaseleccionada,6));
                
            }
        }catch (HeadlessException e){
            JOptionPane.showMessageDialog(null, "Error: "+e+"\nInténtelo nuevamente", " .::Error En la Operacion::." ,JOptionPane.ERROR_MESSAGE);
        }  
        
    
    }//GEN-LAST:event_jButton2ActionPerformed

